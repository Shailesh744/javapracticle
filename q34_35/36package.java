package UserDefinedPackage;

public class MyClass1 {
    public void displayPublic() {
        System.out.println("Public method in MyClass1");
    }

    protected void displayProtected() {
        System.out.println("Protected method in MyClass1");
    }

    void displayDefault() {
        System.out.println("Default method in MyClass1");
    }

    private void displayPrivate() {
        System.out.println("Private method in MyClass1");
    }
}
